#include "get_next_line.h"

t_list	*ft_lstnew(char *content)
{
	t_list	*element;

	element = malloc(sizeof(t_list));
	if (element)
	{
		element->content = content;
		element->next = NULL;
	}
	return (element);
}

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (lst && new)
	{
		if (*lst == NULL)
			*lst = new;
		else
		{
			tmp = ft_lstlast(*lst);
			tmp->next = new;
		}
	}
}

t_list	*ft_lstlast(t_list *lst)
{
	while (lst && lst->next)
	{
		lst = lst->next;
	}
	return (lst);
}

